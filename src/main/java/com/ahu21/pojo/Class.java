package com.ahu21.pojo;

public class Class {
    private String CLASS;

    public String getCLASS() {
        return CLASS;
    }

    public void setCLASS(String CLASS) {
        this.CLASS = CLASS;
    }

    @Override
    public String toString() {
        return "Class{" +
                "CLASS='" + CLASS + '\'' +
                '}';
    }
}
